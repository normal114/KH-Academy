
DROP SEQUENCE MYNOSEQ;

DROP TABLE MYBOARD;

CREATE SEQUENCE MYNOSEQ;

CREATE TABLE MYBOARD(
	MYNO NUMBER PRIMARY KEY,	
	MYNAME VARCHAR2(1000) NOT NULL,
	MYTITLE VARCHAR2(2000) NOT NULL,
	MYCONTENT VARCHAR2(4000) NOT NULL,
	MYDATE DATE NOT NULL,
	VIEWCNT NUMBER NOT NULL
);


INSERT INTO MYBOARD
VALUES(MYNOSEQ.NEXTVAL, '관리자', '스프링 재밌다.', '정말 재밌다.', SYSDATE, 0);


SELECT MYNO, MYNAME, MYTITLE, MYCONTENT, MYDATE, VIEWCNT
FROM MYBOARD
ORDER BY MYNO DESC;

		
